import java.io.*;
public class Logical{
    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;

        //logical operations
        System.out.println("x && y: " + (x && y));
        System.out.println("x || y: " + (x || y));
        System.out.println("!x: " + (!x));

    }


}
