public class Bitwise {
    public static void main(String[] args){
        int d=0b1010;
        int c=0b1100;

        System.out.println("d & c : "+ (d & c));
        System.out.println("d | c : "+ (d | c));
        System.out.println("d ^ c : "+ (d ^ c));
        System.out.println("~d : "+ (~d));
    }
}
