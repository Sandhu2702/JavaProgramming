public class ImplicitCasting {
    public static void main(String[] args){
        int i=6;
        long l;
        l = i;
        double d;
        d = i;
        System.out.println("Original i="+i);
        System.out.println("Casting in long="+l);
        System.out.println("Casting in double="+d);

    }
}
